// Nous allons écrire une boucle qui va faire plusieurs console.log

for (let compteur = 1; compteur <= 100; compteur++) { // compteur++ == compteur += 1
    /* Le premier paramètre de la boucle for est le compteur...
    Le deuxième paramètre est la condition de sortie de la boucle...
    Le troisième paramètre est l'incrémentation du compteur qui va s'effectuer à chaque tour de boucle... */
    console.log(`Bonjour ${compteur} fois !`); // console.log('console.log('Bonjour ' + compteur + ' fois'))
};